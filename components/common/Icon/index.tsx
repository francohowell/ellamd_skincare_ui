export enum IconSize {
  Small,
  Medium,
  Large,
}

interface Props {
  size?: IconSize;
  name: keyof typeof ICON_MAP;
  className?: string;
}
interface State {}

export class Icon extends React.Component<Props, State> {
  public static defaultProps: Partial<Props> = {
    size: IconSize.Small,
  };

  public render() {
    let sizeClass;

    // tslint:disable-next-line switch-default
    switch (this.props.size) {
      case IconSize.Small:
        sizeClass = styles.small;
        break;
      case IconSize.Medium:
        sizeClass = styles.medium;
        break;
      case IconSize.Large:
        sizeClass = styles.large;
        break;
    }

    return (
      <span className={[styles.icon, sizeClass, this.props.className].join(" ")}>
        {ICON_MAP[this.props.name]}
      </span>
    );
  }
}
